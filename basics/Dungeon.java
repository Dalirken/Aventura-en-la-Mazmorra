    /* Meter el mapa en una matriz */
    public void generateDungeonMap() {
        int tile = 0;  // Inicializar `tile` dentro del método
        for (int y = 0; y < dungeonHeight; y++) {
            for (int x = 0; x < dungeonWidth; x++) {
                if (tile < mapAscii.length()) {
                    char currentChar = mapAscii.charAt(tile);
                    // Saltar caracteres de nueva línea que son parte del mapa original
                    if (currentChar != '\r' && currentChar != '\n') {
                        map[y][x] = currentChar;
                        tile++;
                    } else {
                        tile++; // Saltar el salto de línea
                        x--; // Retroceder la columna porque este no es un carácter de la matriz
                    }
                }
            }
        }
    }

    /* Imprimir el mapa */
    public void printDungeonMap() {
        for (int y = 0; y < dungeonHeight; y++) {
            for (int x = 0; x < dungeonWidth; x++) {
                char currentChar = map[y][x];
                    if (currentChar == '╣' || currentChar == '║' || currentChar == '╗' || currentChar == '╝' || currentChar == '╩' || currentChar == '╔' || currentChar == '╦' || currentChar == '╠' || currentChar == '═' || currentChar == '╬' || currentChar == '╚') {
                        // Imprimir en color negro
                        System.out.print(ANSI_BLUE + currentChar + ANSI_RESET);
                    } else if(currentChar == '©') {
                        System.out.print(ANSI_YELLOW + currentChar + ANSI_RESET);
                    } else if(currentChar == '¥'){
                        System.out.print(ANSI_RED + currentChar + ANSI_RESET);
                    } else if(currentChar == 'P' || currentChar == '$'){
                        System.out.print(ANSI_GREEN + currentChar + ANSI_RESET);
                    } else if(currentChar == 'B'){
                        System.out.print(ANSI_PURPLE + currentChar + ANSI_RESET);
                    } else if(currentChar == '░' || currentChar == '▒' || currentChar == '▓'){
                        System.out.print(ANSI_BLUE + currentChar + ANSI_RESET);
                    } else{
                        System.out.print(currentChar); // Imprimir normalmente
                    }
            }
            System.out.println();
        }
    }

    
